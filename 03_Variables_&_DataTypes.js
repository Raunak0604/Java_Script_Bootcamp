// 3. JavaScript Variables

// What are Variables? - Containers to store data values

var number1 = 34;
var number2 = 56;
console.log("The sum is : ", number1 + number2);

// 4. Data types in JavaScript
// Numbers
var num1 = 455;
var num2 = 56.76;
console.log(num1, num2);

// String
var str1 = "This is a string";
var str2 = 'This is also a string';
console.log(str1, str2);

// Objects
var marks = {
    rabbit: 34,
    souvik: 78,
    yashwant: 99.977,
}
console.log(marks);

// Booleans
var a = true;
var b = false;
console.log(a, b);

// Undefine
// var und = undefined;
var und;
console.log(und);

// Null
var n = null;
console.log(n);

// Array
var arr = [1, 2, 3, "rabbit", 4, 5, 6]
console.log(arr);
console.log(arr[3]);

