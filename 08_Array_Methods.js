// Array Methods in Javascript

let myArr = ["Fan", "Camera", 34, null, true];

console.log(myArr.length);
console.log(myArr);

myArr.pop();
console.log(myArr.pop);

myArr.shift()
console.log(myArr);

myArr.push("Rabbit")
console.log(myArr);

let newLen = myArr.unshift("Rabbit")
console.log(newLen);
console.log(myArr);
